def sanitize_code_map(raw_code_map: dict[str, str]):
    clean_code_map = {}
    for key, value in raw_code_map.items():
        key = key.strip("=")
        if key.isdigit():
            key = int(key)
        elif key == "Missing":
            key = np.nan
        clean_code_map[key] = value.strip()
    return clean_code_map


def make_code_dict(codes: str):
    if not isinstance(codes, str):
        return {}
    matches = re.split("([\d]+=|Missing+=)", codes)[1:]
    raw_code_map = dict(zip(matches[0::2], matches[1::2]))
    code_dict = sanitize_code_map(raw_code_map)
    return code_dict


